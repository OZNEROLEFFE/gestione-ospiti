export const STATI: Stato[] = RAW.map(s => {
  const [nome, codice] = s.split('|')
  return { nome, codice }
})

export function getCodiceStato(nome: string): string {
  if (!nome) return ''
  const found = STATI.find(s => s.nome.toUpperCase() === nome.toUpperCase().trim())
  return found?.codice ?? ''
}